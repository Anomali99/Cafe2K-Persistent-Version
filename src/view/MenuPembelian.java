    private void setTotal() {
        DefaultTableModel tbl = (DefaultTableModel) tblMn21.getModel();
        long i = 0;
        for (int a = 0; a < tbl.getRowCount(); a++) {
            i = i + Rupiah.getLong(tbl.getValueAt(a, 4).toString());
        }
        tfTotal.setText(Rupiah.getRp(i));
    }

    private boolean validMenu() {
        boolean valid = true;
        if (tfKode21.getText().equals("")) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Pilih Menu terlebih dahulu");
        } else if (tfJml21.getText().equals("") || tfJml21.getText().equals("0")) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Masukkan jumlah pesanan terlebih dahulu");
        }
        return valid;
    }

    private boolean validPesanan() {
        boolean valid = true;
        if (tfId21.getText().equals("")) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Pilih Pelangan terlebih dahulu");
        } else if (tblMn21.getRowCount() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Pilih Menu terlebih dahulu");
        }
        return valid;
    }

