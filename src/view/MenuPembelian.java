    private void btnSimpan21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpan21ActionPerformed
        if (validPesanan()) {
            Pembelian mod = new Pembelian();
            PembelianPK pk = new PembelianPK();
            pk.setNoPembelian(tfNo1.getText());
            pk.setIdPegawai(MenuUtama.mod.getIdPegawai());
            pk.setIdPelanggan(tfId21.getText());
            mod.setPembelianPK(pk);
            mod.setPegawai(MenuUtama.mod);
            mod.setPelanggan(new DaoPelanggan().getById(tfId21.getText()));
            mod.setTgl(tfTgl.getText());
            mod.setTotal(Rupiah.getBigInt(tfTotal.getText()));
            List<DetailPembelian> list = new ArrayList();
            DefaultTableModel tbl1 = (DefaultTableModel) tblMn21.getModel();
            int index = tbl1.getRowCount() - 1;
            for (int i = 0; i <= index; i++) {
                DetailPembelian p = new DetailPembelian();
                DetailPembelianPK ds = new DetailPembelianPK();
                Menu bn = new DaoMenu().getByKode(tbl1.getValueAt(i, 0).toString());
                ds.setKodeMenu(bn.getKodeMenu());
                ds.setIdPegawai(mod.getPembelianPK().getIdPegawai());
                ds.setIdPelanggan(mod.getPembelianPK().getIdPelanggan());
                ds.setNoPembelian(mod.getPembelianPK().getNoPembelian());
                p.setMenu(bn);
                p.setJumlah(Integer.parseInt(tbl1.getValueAt(i, 3).toString()));
                p.setSubtotal(Rupiah.getBigInt(tbl1.getValueAt(i, 4).toString()));
                p.setDetailPembelianPK(ds);
                p.setPembelian(mod);
                p.setKeterangan(tbl1.getValueAt(i, 5).toString());
                list.add(p);
            }
            mod.setDetailPembelianCollection(list);
            mod.setStatus(cbxStatus.getSelectedItem().toString());
            if (jLabel3.getText().equals("Tambah Pembelian")) {
                servis.tambahData(mod);
            } else {
                servis.ubahData(mod);
            }
            resetTable();
            tfNo.setText(servis.getNomer());
            tfTotal.setText("");
            tbl1.setRowCount(0);
            jLabel3.setText("Tambah Pembelian");
            cbxStatus.setSelectedIndex(0);
            String s = "Pembelian berhasil.\nApakah anda ingin mencetak nota ?";
            ImageIcon icon = new ImageIcon(getClass().getResource("/img/laporan1.png"));
            if (JOptionPane.showConfirmDialog(this, s, "Pemberitahuan", 0, 0, icon) == 0) {
                new DaoLaporan().cetakNotaPembelian(mod);
            }
        }
        jLabel1.requestFocus();
    }//GEN-LAST:event_btnSimpan21ActionPerformed

